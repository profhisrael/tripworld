<hr>
 <p style="color:green;" align="justify">A: It depends on the country you are from. Advertisers pay a very different amount based from where you are from and the ads that are served to you. The amount we get from advertisers also depends on the quality of traffic. Meaning that if the ads are well targeted and you like what you see, click and purchase the products and services, the money you will get will be bigger than if you never click on ads and just ignore them. 

